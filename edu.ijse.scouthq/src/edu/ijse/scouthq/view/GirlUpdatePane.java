    private void setDate(){
        Date dt = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        date = sdf.format(dt);
    }
    
    private void setAllId(){
        try {
            ArrayList<SchoolDTO> schoolDTO = ctrlSchool.getAll();
            for(SchoolDTO schoolDto : schoolDTO){
                schoolIdCmb.addItem(schoolDto.getSclId());
            }
        } catch (Exception ex) {
            Logger.getLogger(BoyRegistrationPane.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
