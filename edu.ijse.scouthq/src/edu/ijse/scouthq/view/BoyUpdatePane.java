    private void loadDetails() {
        
        try {
            MemberDTO memberDTOs = ctrlMbr.search(((String)scoutIdCmb.getSelectedItem()));
            
            nameInitTxtFld.setText(memberDTOs.getNameInit());
            nameFullTxtFld.setText(memberDTOs.getNameFull());
            dateOfBirthTxtFld.setText(memberDTOs.getDateOfBirth());  //date
            addressTxtFld.setText(memberDTOs.getAddress());
            tpTxtFld.setText(""+memberDTOs.getTp());
            SchoolDTO sclName = ctrlSchool.search(memberDTOs.getSclId());
            schoolTxtFld.setText(sclName.getName());
            joiningDateTxtFld.setText(memberDTOs.getJoiningDate());
        } catch (Exception ex) {
            Logger.getLogger(BoyUpdatePane.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
