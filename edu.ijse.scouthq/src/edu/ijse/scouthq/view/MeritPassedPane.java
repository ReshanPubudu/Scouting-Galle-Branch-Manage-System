    public void setAllMeritBadgeId(String type){
            try {
                String cat = "";
                if(type.equals("Cub Scout")){
                    cat = "CSC01";
                }
                if(type.equals("Boy Scout")||type.equals("Girl Scout")){
                    cat = "BSC01";
                }
                if(type.equals("Rover Scout")){
                    cat = "RSC01";
                }
                ArrayList<MeritBadgeDTO> meritBadgeDTO = new MeritBadgeDAOImpl().searchAndGetAllId(cat);
                for(MeritBadgeDTO meritBadgeDTO1 : meritBadgeDTO){
                    badgeIdCmb.addItem(meritBadgeDTO1.getBadgeId());
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(BoyRegistrationPane.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    

