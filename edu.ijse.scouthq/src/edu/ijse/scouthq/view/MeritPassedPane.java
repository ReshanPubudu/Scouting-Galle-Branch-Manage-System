    private void badgeIdCmbItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_badgeIdCmbItemStateChanged
        viewWhatPane.setVisible(false);
        try {
            String badgeId = (String) badgeIdCmb.getSelectedItem();
            MeritBadgeDTO meritBadgeDTO = ctrlMerit.search(badgeId);
            String badgeName = meritBadgeDTO.getBadgeName();
            badgeNameTxt.setText(badgeName);
            teacherIdCmb.removeAllItems();
            /////////////////////////////////////////////////////////////
            
            
            
            ArrayList<MeritBadgeTeacherDTO> meritBadgeTeacherDTOs = new MeritBadgeTeacherDAOImpl().searchAndGetAllId(badgeId);
            for(MeritBadgeTeacherDTO meritBadgeTeacherDTO : meritBadgeTeacherDTOs){
                teacherIdCmb.addItem(meritBadgeTeacherDTO.gettId());
            }
            
            
            
            /////////////////////////////////////////////////////////////
        } catch (Exception ex) {
            Logger.getLogger(CorePassedPane.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_badgeIdCmbItemStateChanged

