    private void checkMouseEvent(int clicked, int event){
        JLabel catLbl = new JLabel();
        switch (clicked) {
            case 10:
                catLbl = cubScoutCoreLbl;
                break;
            case 11:
                catLbl = boyScoutCoreLbl;
                break;
            case 12:
                catLbl = roverScoutCoreLbl;
                break;
            default:
                break;
        }
        switch (event) {
            case 1:
                checkMouseEvent(clickedLbl,4);
                mouseClick(catLbl);
                clickedLbl = clicked;
                break;
            case 2:
                if(clickedLbl !=clicked){
                    mouseEnted(catLbl);
                }   break;
            case 3:
                if(clickedLbl !=clicked){
                    mouseExited(catLbl);
                }   break;
            case 4:
                mouseExited(catLbl);
                break;
            default:
                break;
        }
    }
    
    private void mouseClick(JLabel catLbl){
        catLbl.setBackground(new Color(80, 80, 250));
        catLbl.setForeground(Color.white);
    }
    
    private void mouseEnted(JLabel catLbl){
        catLbl.setBackground(new Color(100, 140, 250));
        catLbl.setForeground(Color.black);
    }
    
    private void mouseExited(JLabel catLbl){
        catLbl.setBackground(new Color(170,204,255));
        catLbl.setForeground(Color.black);
    }
    
