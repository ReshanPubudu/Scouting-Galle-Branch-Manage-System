    private void finishBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finishBtnActionPerformed
        int i = 0;
        
        String nameInit = nameInitTxtFld.getText().trim();
        String nameFull = nameFullTxtFld.getText().trim();
        
        String dateOfBirthMonth = getMonthNo((String) birthMonthCmb.getSelectedItem());
        String dateOfBirth = birthYearCmb.getSelectedItem()+"-"+dateOfBirthMonth+"-"+birthDayCmb.getSelectedItem();
        String address = addressTxtFld.getText().trim();
        String tp  = tpTxtFld.getText().trim();
        String sclId = (String) schoolIdCmb.getSelectedItem();
        String joinMonth = getMonthNo((String) joinMonthCmb.getSelectedItem());
        String joiningDate = joinYearCmb.getSelectedItem()+"-"+joinMonth+"-"+joinDayCmb.getSelectedItem();
        
        if(nameFull.matches("([A-Za-z]([\\s]*)?)*")){
            i++;
        }
        else{
            JOptionPane.showMessageDialog(this,"Sorry, Pleace enter a valid Name");
        }
        
        if(nameInit.equals("")){
            nameInit = nameFull;
        }
        
        if(!address.equals(null)){
            i++;
        }
        
        if(tp.matches("[0]\\d{9}")){
            i++;
        }
        else{
            JOptionPane.showMessageDialog(this,"Sorry, Pleace enter a valid Telephon Number");
        }
        
        if(i==3){ 
            ConformationForm.nameInit = nameInit;
            ConformationForm.nameFull = nameFull;
            ConformationForm.dateOfBirth = dateOfBirth;
            ConformationForm.address = address;
            ConformationForm.tp = tp;
            ConformationForm.sclId = sclId;
            ConformationForm.joiningDate = joiningDate;
            ConformationForm.registedDate = date;    // date

            ConformationForm.sc = "Cub Scout";
            new ConformationForm().setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(this,"Sorry, Pleace Check and Re-enter a Valied Details");
        }
    }//GEN-LAST:event_finishBtnActionPerformed

